    private void potvrdithraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_potvrdithraActionPerformed
     if (!testBezi) return;
     pocet++;
     String odpoved = hratext.getText().trim();
     boolean spravne = false;
     
     if (vyber == 0 && odpoved.equalsIgnoreCase(p.sk)) {
         spravne = true;
     } else if (vyber == 1 && odpoved.equalsIgnoreCase(p.en)) {
         spravne = true;
    }

    if (spravne) {
        body++;
        if(max <= body) max =  (int)body;
        spravneodpovedi.setText(""+(int) body);
        nespravnetxt.setText("Správne!");
        skoremax.setText(""+max);
    } else {
        nesprav++;
        pocetnespravnych.setText(""+nesprav);
        nespravnetxt.setText("Zle! Správne bolo: " + (vyber == 0 ? p.sk : p.en));
    }
    
     uspesnostc = (int) ((body/ pocet)*100);
     uspesnost.setText(uspesnostc + " %");

    guess();
    }//GEN-LAST:event_potvrdithraActionPerformed
    
    private void editDocu(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editDocu
    try {
        // Získaj cestu k súboru slovnik.txt na ploche
        String cestaPlocha = System.getProperty("user.home") + File.separator + "Desktop";
        File subor = new File(cestaPlocha + File.separator + "slovnik.txt");
        // Ak súbor ešte neexistuje, vytvor ho (ale neprepisuj)
        if (!subor.exists()) {
            subor.createNewFile();
        }
        
        // Otvor súbor v predvolenom textovom editore
        if (Desktop.isDesktopSupported()) {
            Desktop desktop = Desktop.getDesktop();
            desktop.open(subor);
            readFile();
        } else {
            System.out.println("Desktop nie je podporovaný na tomto systéme.");
        }

    } catch (IOException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_editDocu

