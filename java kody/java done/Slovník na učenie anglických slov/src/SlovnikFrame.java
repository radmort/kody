    public void guess()
	{
        Random r = new Random();
		
        if (ps.isEmpty()) {
        zadanie.setText("Slovník je prázdny.");
        return;
            }
       
         int i = r.nextInt(ps.size());
         result:
         if(i==last){
             i = r.nextInt(ps.size());
             break result;
         }
         last = i;
         p = ps.get(i);
         String f = (vyber == 0) ? p.en : p.sk;
         zadanie.setText(f);
	}
	
    
    public  void readFile()
    {
     String[] ls = ReadWriter.readLines(ReadWriter.getFile("slovnik"));	
     int count = 0;
     pocetDvojic = ls.length;
     for (int i = 0; i < ls.length; i++)
     {
      String[] sp = ls[i].split(" ");
      if(sp.length == 2)
      {
       ps.add(new Pair(sp[0], sp[1]));
       count = i+1;
      }
      
     }
      dvojicePocet.setText(""+count);
    }
    
